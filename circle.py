import math


def area(r):
    '''Принимает радиус окружности r, возвращает площадь окружности'''
    '''Пример вызова'''
    '''Input: 6'''
    '''Output: 59.1576'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает радиус окружности r, возвращает длину окружности'''
    '''Пример вызова'''
    '''Input: 6'''
    '''Output: 37.68'''
    return 2 * math.pi * r

