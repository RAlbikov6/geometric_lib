def area(a, b):
    '''Принимает 2 стороны прямоугольника a и b, возращает площадь прямоугольника
    Пример вызова
    Input: 5 10
    Output: 50'''
    return a * b 

def perimeter(a, b):
    '''Принимает 2 стороны прямоугольника a и b, возращает периметр прямоугольника
    Пример вызова
    Input: 5 10
    Output: 30'''
    return 2*(a + b)

