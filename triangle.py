def area(a, h):
    '''Принимает сторону треугольника 'a' и высоту прилегающую к этой стороне 'h', возвращает площадь треугольника
    Пример вызова
    Input: 5 4
    Output: 10'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает стороны треугольника a,b,c и возвращает периметр треугольника
    Пример вызова
    Input: 7 7 7
    Output: 21'''
    return a + b + c

